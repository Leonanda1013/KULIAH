public class DoWhile {
    public static void main(String[] args) {
        int x = 0; //ini merupakan inisialisasi pda system do-while
        do {
            System.out.println(x);

        }while(+x <= 8);//ini merupakan kondisi pada while, yang menunjukkan kondisi bahwa jika x belum mencapai 8 akan terjadi perulangan.
        System.out.println("Program berhenti");//ini merupakan output yang akan dikeluarkan
    }
    
}
